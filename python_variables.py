# Creating variables
x = 5
y = "John"
print(x)
print(y)

# Change variable type
x = "Smith"
print(x)

# Casting
x = str(3)      # x will be '3'
y = int(3)      # y will be 3
z = float(3)    # z will be 3.0
print(x)
print(y)
print(z)

# Get the Type
